import freeCodeCampLogo from "./imagenes/freecodecamp-logo.png";
import "./App.css";
import Boton from "./componentes/Boton";
import Pantalla from "./componentes/Pantalla";
import BotonClear from "./componentes/BotonClear";
import { useState } from "react";
import { evaluate } from "mathjs"; /* es una libreria que sirve para hacer operaciones matematicas.*/

function App() {
  const [input, SetInput] = useState("");

  const agregarInput = (val) => {
    SetInput(input + val);
  };

  const calcularResultado = () => {
    if (input) {
      /* se puede generar este valor porque existen los valores truthy y falsy que son valores que por si solos son verdaderos o falsos// las cadenas de caracteres vacías son falsas y sino son verdadaderas*/
      SetInput(evaluate(input));
    } else {
      alert("por favor ingrese valores para realizar los cálculos");
    }
  };
  return (
    <div className="App">
      <div className="freecodecamp-logo-contenedor">
        <img
          src={freeCodeCampLogo}
          className="freecodecamp-logo"
          alt="Logo de freeCodeCamp"
        />
      </div>
      <div className="contenedor-calculadora">
        <Pantalla input={input} />
        <div className="fila">
          <Boton manejarClic={agregarInput}>1</Boton>
          <Boton manejarClic={agregarInput}>2</Boton>
          <Boton manejarClic={agregarInput}>3</Boton>
          <Boton manejarClic={agregarInput}>+</Boton>
        </div>
        <div className="fila">
          {" "}
          <Boton manejarClic={agregarInput}>4</Boton>
          <Boton manejarClic={agregarInput}>5</Boton>
          <Boton manejarClic={agregarInput}>6</Boton>
          <Boton manejarClic={agregarInput}>-</Boton>
        </div>
        <div className="fila">
          <Boton manejarClic={agregarInput}>7</Boton>
          <Boton manejarClic={agregarInput}>8</Boton>
          <Boton manejarClic={agregarInput}>9</Boton>
          <Boton manejarClic={agregarInput}>*</Boton>
        </div>
        <div className="fila">
          <Boton manejarClic={calcularResultado}>=</Boton>
          <Boton manejarClic={agregarInput}>0</Boton>
          <Boton manejarClic={agregarInput}>.</Boton>
          <Boton manejarClic={agregarInput}>/</Boton>
        </div>
        <div className="fila">
          <BotonClear manejarClear={() => SetInput("")}>Clear</BotonClear>
        </div>
      </div>
    </div>
  );
}

export default App;
